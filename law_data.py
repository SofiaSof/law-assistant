def search_in_law(query: str) -> list:
    """Poisk po zakonu"""
    query = query.lower()
    results = []
    
    for art_num, art_data in LAW_CONTENT["articles"].items():
        content = art_data["content"].lower()
        title = art_data["title"].lower()
        
        if query in content or query in title:
            results.append({
                "article": art_num,
                "title": art_data["title"],
                "content": art_data["content"]
            })
    
    return results


