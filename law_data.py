def semantic_search(query: str) -> list:
    """Semanticheskiy poisk po zakonu"""
    query = query.lower()
    query_words = set(query.split())
    
    scored_results = []
    
    for art_num, keywords in SEMANTIC_MAP.items():
        score = 0
        matched_keywords = []
        
        for keyword in keywords:
            keyword_lower = keyword.lower()
            if keyword_lower in query:
                score += 10
                matched_keywords.append(keyword)
            for word in query_words:
                if word in keyword_lower or keyword_lower in word:
                    score += 1
        
        if score > 0:
            article_data = get_article(art_num)
            if article_data:
                scored_results.append({
                    "article": art_num,
                    "title": article_data["title"],
                    "content": article_data["content"],
                    "score": score,
                    "matched": matched_keywords
                })
    
    scored_results.sort(key=lambda x: x["score"], reverse=True)
    return scored_results


def get_best_match(query: str) -> dict:
    """Poluchit luchshuyu statyu po semanticheskomu zaprosu"""
    results = semantic_search(query)
    if results:
        return results[0]
    return None


